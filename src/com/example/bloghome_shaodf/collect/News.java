package com.example.bloghome_shaodf.collect;

public class News {
	private int id;
	private String title;
	private String sum;   
	private String publish;  
	private String update;   
	private String link_hr;
	private int digs;    
	private int views;  
	private int comments;  
	private String topic;
	private String topicIcon;
	private String srcName;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getSum() {
		return sum;
	}
	public void setSum(String sum) {
		this.sum = sum;
	}
	public String getPublish() {
		return publish;
	}
	public void setPublish(String publish) {
		this.publish = publish;
	}
	public String getUpdate() {
		return update;
	}
	public void setUpdate(String update) {
		this.update = update;
	}
	public String getLink_hr() {
		return link_hr;
	}
	public void setLink_hr(String link_hr) {
		this.link_hr = link_hr;
	}
	public int getDigs() {
		return digs;
	}
	public void setDigs(int digs) {
		this.digs = digs;
	}
	public int getViews() {
		return views;
	}
	public void setViews(int views) {
		this.views = views;
	}
	public int getComments() {
		return comments;
	}
	public void setComments(int comments) {
		this.comments = comments;
	}
	public String getTopic() {
		return topic;
	}
	public void setTopic(String topic) {
		this.topic = topic;
	}
	public String getTopicIcon() {
		return topicIcon;
	}
	public void setTopicIcon(String topicIcon) {
		this.topicIcon = topicIcon;
	}
	public String getSrcName() {
		return srcName;
	}
	public void setSrcName(String srcName) {
		this.srcName = srcName;
	}
	
}
