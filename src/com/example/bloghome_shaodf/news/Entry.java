package com.example.bloghome_shaodf.news;

public class Entry {

	String id;
	String title;
	String summary;
	String updated;
	String aythor_name;
	String link_href;
	String views;
	String comments;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getSummary() {
		return summary;
	}
	public void setSummary(String summary) {
		this.summary = summary;
	}
	public String getUpdated() {
		return updated;
	}
	public void setUpdated(String updated) {
		this.updated = updated;
	}
	public String getAythor_name() {
		return aythor_name;
	}
	public void setAythor_name(String aythor_name) {
		this.aythor_name = aythor_name;
	}
	public String getLink_href() {
		return link_href;
	}
	public void setLink_href(String link_href) {
		this.link_href = link_href;
	}
	public String getViews() {
		return views;
	}
	public void setViews(String views) {
		this.views = views;
	}
	public String getComments() {
		return comments;
	}
	public void setComments(String comments) {
		this.comments = comments;
	}
}
